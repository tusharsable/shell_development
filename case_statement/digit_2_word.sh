#!/bin/bash -x

#read number from user
read -p "Enter single digit number" number

#check for number in each case and print accordingly
case $number in
	
	1)
	echo "One"
	;;
	
	2)
	echo "Two"
	;;

	3)
	echo "Three"
	;;

	4)
	echo "Four"
	;;

	5)
	echo "five"
	;;

	6)
	echo "Six"
	;;

	7)
	echo "Seven"
	;;

	8)
	echo "Eight"
	;;

	9)
	echo "Nine"
	;;

esac
